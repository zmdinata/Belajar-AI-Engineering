//Menggunakan For Loop
for (let i = 1; i <= 5; i++) {
    console.log("Perulangan ke-" + i);
}

console.log("-----");
//Penjelasan: For loop digunakan ketika jumlah iterasi sudah diketahui sebelumnya.

//Menggunakan While Loop
let count = 1;
while (count <= 5) {
    console.log("Perulangan ke-" + count);
    count++;
}

console.log("-----");
//Penjelasan: While loop akan terus menjalankan blok kode selama kondisi bernilai true.

//Menggunakan Do...While Loop
let angka = 1;
do {
    console.log("Perulangan ke-" + angka);
    angka++;
} while (angka <= 5);

console.log("-----");
//Penjelasan: Do...While loop akan menjalankan blok kode minimal satu kali sebelum memeriksa kondisi.

